#使用\n进行换行
print('注册会员需要以下信息：\n姓名\n年龄\n手机号')

#使用\\输出\
print('D:\\nice')

#使用\b删除前一个字符
print('helloo\b')

#使用\r使用光标回到本行开头，覆盖输出
print('67%\r68%')

#%t：水平制表符（让光标跳转到下一个制表位）
